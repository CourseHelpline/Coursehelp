<h1>OBJECTIVE</h1>
<p style="font-size:15pt;color:#DAA520" class="link">The basic aim of the college of dentistry is health-oriented teaching that emphasizes the prevention of oral diseases. All students are inculcated with the enthusiasm for not just curing dental diseases but also their prevention. The college actively spreads this message in the surrounding community. An experienced faculty team is always accessible to the students.<br>

The dental college offers courses for the undergraduates B.D.S. (5 year program) as well as the postgraduate M.D.S. (3 year program in 6 different specialties).<br><p>

<h1>OUTREACH PROGRAMS</h1>
<p style="font-size:15pt;color:#DAA520" class="link">
Students find the regular camps we conduct in schools and rural areas a fulfilling experience. These camps form part of the community dentistry program. The camps spread awareness of the importance of dental health, and free toothpastes, gum paint and mouthwash are given to the needy.<br>
