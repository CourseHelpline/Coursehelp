<h1>OBJECTIVES</h1>
<p style="font-size:15pt;color:#DAA520" class="link">As new and rapid techniques of dealing with diseases at the molecular level are discovered, students need to be prepared and possess a passion for research. The 3-year Masters in Medical Biochemistry equips students with all the skills required to enter the fascinating world of biochemistry research, and opens up many opportunities in this rapidly growing field.<br>

Professors rely on advanced teaching methodologies and learning aids to ensure students learn the subject in an interesting way. The faculty's experience widens the scope for both clinical biochemistry and research. They constantly stay updated with the latest developments in the field by involving themselves in conferences and symposia. The department also has a well-knit team of health sciences and technical staff who can take credit for the smooth functioning of the laboratories.<br><p>

<h1>CURRICULUM</h1>
<p style="font-size:15pt;color:#DAA520" class="link">
M.B.B.S. students are initially instructed in the basics, giving them an understanding of the clinical processes involved in biochemistry. Case discussions are used to instruct students in a challenging way. Students are constantly assessed and given additional guidance, as they require it. During clinical postings, students can familiarize themselves with the facilities available in the clinical laboratories. Students also receive hands on training in estimating biochemical parameters, while gaining practical experience in the point-of-care testing of patients at the bedside to assess their condition.
<br></p>
<br>
<h1>RESEARCH AND PROJECTS</h1>
<p style="font-size:15pt;color:#DAA520" class="link">
Postgraduate students are encouraged to take up research in the various fields of biochemistry. The research laboratory is a boon as it has the resources to allow students to perform clinical research. The department has several projects on the anvil:<br>

Establish normal clinical levels in rural population in and around the villages surrounding Kattankulathur<br>
Study the prevalence of inborn errors of metabolism<br>
Assess biochemical parameters in the geriatric population in and around the villages surrounding Kattankulathur<br>
Screen herbal drugs for glycemic and lipid control in animal studies<br>
